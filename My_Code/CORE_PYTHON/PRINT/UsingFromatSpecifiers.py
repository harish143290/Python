''' using format specifiers
      format specifiers are used to rep what type values
      to be presented during output
      int     -> %d or %i
      float -> %f
      str    -> %s

 print("format specifier" %variable)
 print("format specifiers" %(variables) )
'''
x=10 #int
y=12.12 #float
z="Shashi" #str

print("%d" %x)
print("x val is : %d" %x)
print("y val is : %f " %y)
print("y val is : %.2f" %y)

print("x val is :%i , y val is :%f and z val :%s " %(x,y,z))










