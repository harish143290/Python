''' Relational operators are used to check the relation
between the operands and it will return bool value
               > | >= | < | <= | == | !=  '''

x,y=10,5
print("x=10 and  y=5")
print("x>y     ? : ",x>y)
print("x>=y   ? : ",x>=y)

print("x<y     ? : ",x<y)
print("x<=y   ? : ",x<=y)

print("x==y   ? : ",x==y)
print("x!=y    ? : ",x!=y)

