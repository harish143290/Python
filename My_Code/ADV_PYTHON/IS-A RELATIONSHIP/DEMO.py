#         <class>   <SuperClsName>:
#             Fields
#             Methods
#
#         <class>   <SubClsName>(<SuperClsName>):
#             Fields
#             Methods
#
# * private members are not inherited
# * Inherited Members are acts as regular members of
#    that class.
# * Class and static methods are also inherited.
