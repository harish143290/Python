# Method Overriding:
#     It is the process of defining sub class method
#     same as super class method.In this case priority
#     is given to sub class method only.
#
#     if u want call super class method then we have to
#     use following method
#
#     ''' super( ).methodname([list of args]) '''
#
#     When do we go for method overriding ?
#       Ans : Whenever u want use the super class method
#       into the subclass with different implementation.
